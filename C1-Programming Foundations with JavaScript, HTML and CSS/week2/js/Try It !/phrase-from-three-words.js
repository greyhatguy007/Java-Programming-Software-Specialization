function phrase3words(v1, v2, v3) {
    var ans = v1 + " " + v2 + " " + v3;
    return ans;
}

var result1 = phrase3words("smile", "at", "everyone");
var result2 = phrase3words("everyone", "wave", "back");
var result3 = phrase3words("coding", "is", "fun");

console.log(result1);
console.log(result2);
console.log(result3);

//  OUTPUT:
//  smile at everyone
//  everyone wave back
//  coding is fun